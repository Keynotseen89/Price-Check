/**
 *  Update SERVICE with type of email server
 *  Update PORT with different port if needed
 *  Update USER with your email username 
 *  Update PASS with email password or auth key
 */
export enum AccountSetting{
    SERVICE = 'gmail',
    HOST = '',
    PORT = 587,
    USER = '<user-name-for-email-account>',
    PASS = '<password-or-key-for-login>',
}